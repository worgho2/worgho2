    /**
     * This functions build the edge list for a given board type.
     */
    pub fn get_edge_model(&self) -> Vec<Vec<Vec<(i8, i8)>>> {
        let group_coordinates = self.get_group_coordinates();
        let group_coordinates_ref: Vec<&Vec<(i8, i8)>> = group_coordinates.iter().collect();

        let mut coordinate_index_to_group_coordinates_map: HashMap<String, &Vec<(i8, i8)>> =
            HashMap::new();

        for group in group_coordinates_ref {
            for coordinate in group {
                let coordinate_index = format!("{}_{}", coordinate.0, coordinate.1);
                coordinate_index_to_group_coordinates_map.insert(coordinate_index, group);
            }
        }

        let order = self.get_order();

        let mut edge_model: Vec<Vec<Vec<(i8, i8)>>> = (0..order)
            .map(|_| (0..order).map(|_| vec![]).collect())
            .collect();

        for i in 0..order {
            for j in 0..order {
                let mut coordinate_set: HashSet<String> = HashSet::new();

                for k in 0..order {
                    coordinate_set.insert(format!("{}_{}", k, j));
                    coordinate_set.insert(format!("{}_{}", i, k));
                }

                let group_coordinates = coordinate_index_to_group_coordinates_map
                    .get(&format!("{}_{}", i, j))
                    .unwrap();

                for coordinate in group_coordinates.iter() {
                    coordinate_set.insert(format!("{}_{}", coordinate.0, coordinate.1));
                }

                coordinate_set.remove(&format!("{}_{}", i, j));

                edge_model[i as usize][j as usize] = coordinate_set
                    .iter()
                    .map(|coordinate_index| {
                        let coordinate_index = coordinate_index.split("_").collect::<Vec<&str>>();
                        (
                            coordinate_index[0].parse::<i8>().unwrap(),
                            coordinate_index[1].parse::<i8>().unwrap(),
                        )
                    })
                    .collect();
            }
        }

        edge_model
    }

